package com.example.vchichvarin.weatherforecast.data;

public class Coord {

    private float lat;
    private float lon;

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLng(float lng) {
        this.lon = lng;
    }
}
